package com.um.classify;

public class DiagMapping {
	
	private String cnDiagClass;
	
	private String wnDiagClass;
	
	private int count;
	
	

	public DiagMapping() {
		
	}

	public String getCnDiagClass() {
		return cnDiagClass;
	}

	public void setCnDiagClass(String cnDiagClass) {
		this.cnDiagClass = cnDiagClass;
	}

	public String getWnDiagClass() {
		return wnDiagClass;
	}

	public void setWnDiagClass(String wnDiagClass) {
		this.wnDiagClass = wnDiagClass;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
	
	
	
}
